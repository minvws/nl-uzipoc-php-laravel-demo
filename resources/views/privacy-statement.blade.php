                            URA-nummer (uniek nummer organisatie)
                        </li>
                        <li>
                            Rolcode(s) (bevoegdheden, beroep)
                        </li>
                        <li>
                            Voorletter(s) en voornaam/voornamen
                        </li>
                        <li>
                            Achternaam inclusief tussenvoegsel(s)
                        </li>
                    </ul>
                </li>
                <li>
                    <strong>BSN</strong>
                    <p>
                        Uw BSN verwerken wij als u inlogt via DigiD en ontvangen wij bij een dergelijke inlog van het Ministerie van Binnenlandse Zaken (uitgevoerd door Logius).
                    </p>
                </li>
                <li>
                    <strong>IP-adres</strong>
                    <p>
                        Dit is het IP adres van de computer die u gebruikt en dit is nodig om een technische verbinding op te bouwen met deze website.
                    </p>
                </li>
            </ul>
            <h3>Op grond waarvan verwerken wij uw persoonsgegevens?</h3>
            <p>
                Voor het gebruik van uw persoonsgegevens in deze pilot hebben wij uw toestemming gekregen. U kunt deze intrekken, wat betekent dat de pilot voor u zal stoppen.
                Als u inlogt met Yivi, zal ook SIDN u om toestemming vragen. U wordt ook gevraagd om akkoord te gaan met hun algemene voorwaarden. Uiteraard staat het u vrij geen toestemming te geven, wat betekent dat u niet met Yivi in kunt loggen. Ook geldt dat als u de Yivi-app van uw telefoon verwijdert u de toestemming effectief intrekt.
            </p>

            <h2>Beveiliging van uw persoonsgegevens</h2>
            <p>
                De belangrijkste maatregel die wij genomen hebben is dat uw persoonsgegevens versleuteld zijn, zowel bij opslag, uitwisseling als gebruik van deze persoonsgegevens (“end-to-end”).
            </p>
            <p>
                Over de beveiliging van onze ketenpartners leest u meer in hun privacyverklaringen. De links vindt u  hierboven. Zo kunt u lezen dat SIDN ISO 27001-gecertificeerd is.
            </p>


            <h2>Hoe lang worden uw persoonsgegevens door VWS bewaard?</h2>
            <p>
                VWS bewaart uw persoonsgegevens niet langer dan nodig is met oog op het doel van deze pilot.
            </p>
