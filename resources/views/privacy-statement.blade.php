            <h1>Uw privacy</h1>

            <p>
                Met uw deelname aan de tweede pilot vanuit het project ‘Toekomstbestendig maken UZI-middelen’ verwerkt het Ministerie van Volksgezondheid, Welzijn en Sport (<strong>VWS</strong>) uw persoonsgegevens. Wij werken in overeenstemming met ons privacy beleid zoals beschreven in de <a rel="external" href="https://www.rijksoverheid.nl/ministeries/ministerie-van-volksgezondheid-welzijn-en-sport/privacy">Privacyverklaring van VWS</a>.  Hieronder vindt u aanvullende informatie over de verwerking van uw persoonsgegevens.
            </p>

            <h2>Voor welke doeleinden worden uw persoonsgegevens verwerkt?</h2>
            <p>
                In deze pilot krijgen deelnemende zorgverleners alternatieve inlogmiddelen voor de systemen BRBA en NCPeH. VWS toetst hiermee het identificeren en authentiseren met deze alternatieve inlogmiddelen, voor het betrouwbaar en gebruikersvriendelijk verkrijgen van toegang tot BRBA en NCPeH.
            </p>

            <h2>Welke inlogmiddelen worden gebruikt en wie is daarbij verantwoordelijk voor de verwerking van uw persoonsgegevens?</h2>
            <p>
                Binnen deze pilot kunt u inloggen via:
            </p>
            <ul>
                <li>
                    <strong>DigiD</strong>
                    <p>De minister van Binnenlandse Zaken en Koninkrijksrelaties is verantwoordelijk voor het verwerken van persoonsgegevens voor DigiD. <a rel="external" href="https://www.digid.nl/over-digid/privacy/">Hier</a> leest u hoe uw gegevens beschermd worden bij het gebruik van Digid.</p>
                </li>
                <li>
                    <strong>UZI-pas</strong>
                    <p>Het CIBG treedt onder verantwoordelijkheid van VWS op als beheerder/verstrekker van de UZI-gegevens.<br><a rel="external" href="https://www.uziregister.nl/privacy">Hier</a> leest u hoe uw gegevens beschermd worden bij de verstrekking van uw UZI-gegevens door het CIBG. Deze verstrekking vindt ook plaats bij gebruik van Digid en Yivi, dus niet alleen bij het gebruik van de UZI-pas.</p>
                </li>
                <li>
                    <strong>YIVI</strong>
                    <p>Yivi is een identiteitsapp van SIDN Business B.V. (onderdeel van Stichting Internet Domeinregistratie Nederland, SIDN). Lees verder onder <a href="#more-about-yivi">Meer over Yivi</a>. SIDN is verantwoordelijk voor verwerken van persoonsgegevens door Yivi. Hier leest u hoe uw gegevens beschermd worden bij het gebruik van Yivi.</p>
                </li>
            </ul>
            <p>
                VWS verwerkt uw persoonsgegevens om het mogelijk te maken om met de drie verschillende inlogmiddelen toegang te krijgen tot BRBA en NCPeH. VWS heeft hiertoe een technische koppeling ontwikkeld. De hierbij verwerkte persoonsgegevens zijn versleuteld.
            </p>

            <h2 id="more-about-yivi">Meer over Yivi</h2>
            <p>
                Zoals u kunt lezen op <a rel="external" href="https://www.yivi.app/">https://www.yivi.app/</a> is de Yivi app ontwikkeld door SIDN, een non-profit identiteitsdienstverlener. Na het downloaden van deze app op uw telefoon, haalt u via de app uw UZI-kenmerken en naam op, waarmee u vervolgens inlogt.
            </p>
            <p>
                De opzet van Yivi is zodanig dat persoonsgegevens/attributen, exclusief bij de gebruiker zelf in de Yivi-app op de eigen telefoon of tablet versleuteld worden opgeslagen en dat de gebruiker zelf de controle heeft over met wie hij zijn persoonsgegevens deelt.
            </p>
            <p>
                SIDN kan niet zien dat de gebruiker de UZI gegevens bij het CIBG ophaalt en deze deelt met BRBA of NCPeH.
            </p>
            <p>
                SIDN verzamelt en bewaart een aantal gegevens ten behoeve van de registratie van nieuwe Yivi-gebruikers. Daarnaast slaat SIDN een beperkte verzameling historische gebruiksgegevens op. <a rel="external" href="https://www.yivi.app/privacy">Hier</a> leest u meer over uw privacy bij het gebruik van Yivi.
            </p>

            <h2>Welke persoonsgegevens worden door VWS verwerkt?</h2>
            <ul>
                <li>
